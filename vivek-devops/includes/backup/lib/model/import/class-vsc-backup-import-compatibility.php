class VSC_Backup_Import_Compatibility {

	public static function execute( $params ) {

		// Set progress
		VSC_Backup_Status::info( __( 'Checking extensions compatibility...', VSC_BACKUP_PLUGIN_NAME ) );

		// Get messages
		$messages = VSC_Backup_Compatibility::get( $params );

		// Set messages
		if ( empty( $messages ) ) {
			return $params;
		}

		// Error message
		throw new VSC_Backup_Compatibility_Exception( implode( $messages ) );
	}
}
