class VSC_Migration_Import_Clean {

	public static function execute( $params ) {
		// Get database client
		$db_client = VSC_Migration_Database_Utility::create_client();

		// Flush mainsite tables
		$db_client->add_table_prefix_filter( vsc_migration_table_prefix( 'mainsite' ) );
		$db_client->flush();

		// Trigger import cancel action
		if ( isset( $params['vsc_migration_import_cancel'] ) ) {
			do_action( 'vsc_migration_status_import_canceled', $params );
		} else {
			do_action( 'vsc_migration_status_import_done', $params );
		}

		// Delete storage files
		VSC_Migration_Directory::delete( vsc_migration_storage_path( $params ) );

		// Exit in console
		if ( defined( 'WP_CLI' ) ) {
			return $params;
		}

		exit;
	}
}
