class VSC_Migration_Export_Compatibility {

	public static function execute( $params ) {

		// Set progress
		VSC_Migration_Status::info( __( 'Checking for compatibility...', 'vivek-devops' ) );

		// Get messages
		$messages = VSC_Migration_Compatibility::get( $params );

		// Set messages
		if ( empty( $messages ) ) {
			return $params;
		}

		// Error message
		throw new VSC_Migration_Compatibility_Exception( wp_kses( implode( $messages ), vsc_migration_allowed_html_tags() ) );
	}
}
