class VSC_Migration_Export_Archive {

	public static function execute( $params ) {

		do_action( 'vsc_migration_status_export_start', $params );

		// Set progress
		VSC_Migration_Status::info( __( 'Creating export file...', 'vivek-devops' ) );

		// Create empty archive file
		$archive = new VSC_Migration_Compressor( vsc_migration_archive_path( $params ) );
		$archive->close();

		// Set progress
		VSC_Migration_Status::info( __( 'Export file created.', 'vivek-devops' ) );

		return $params;
	}
}
